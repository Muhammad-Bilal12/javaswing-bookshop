    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        DB_Connection db = new DB_Connection();

        DefaultTableModel tb = (DefaultTableModel) bookTable.getModel();

        if (db.deleteData(search.getText()) == 1) {

            JOptionPane.showMessageDialog(this, "Data delete Successfully!!", "Data delete", 1);

            tb.setRowCount(0);
            addrowData();
            search.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "ERROR!! Some error Occured", "Data delete", 2);

        }

    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        DB_Connection db = new DB_Connection();

        DefaultTableModel tb = (DefaultTableModel) bookTable.getModel();

        if (db.updateData(search.getText(), bookname.getText(), txt_edition.getText(), txt_price.getText()) == 1) {

            JOptionPane.showMessageDialog(this, "Data Update Successfully!!", "Data Updated", 1);

            tb.setRowCount(0);
            addrowData();

            search.setText("");
            bookname.setText("");
            txt_edition.setText("");
            txt_price.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "ERROR!! Some error Occured", "Data Updated", 2);

        }

    }//GEN-LAST:event_jButton4ActionPerformed

