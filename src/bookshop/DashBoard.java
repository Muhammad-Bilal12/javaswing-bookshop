    public void addrowData() {
        ResultSet rs;
        DB_Connection db = new DB_Connection();
        rs = db.getBooks();
        System.out.println(rs);
        try {
            DefaultTableModel tb = (DefaultTableModel) bookTable.getModel();

            while (rs.next()) {

                String[] booksData = {rs.getString("id"), rs.getString("name"), rs.getString("edition"), rs.getString("price")};
                tb.addRow(booksData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DashBoard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

